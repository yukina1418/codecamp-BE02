/* 

    시간복잡도(Big-O)는 어떠한 알고리즘 또는 로직이 실행될 때에
    해당 로직이 전부 실행되는데에 얼마만큼의 시간이 필요한지를 결정합니다.

    Big-O 의 표기법은 알고리즘의 시간적인 성능을 수학적으로 표현하기 위한 표기법이며,
    이 표기법은 단순하게 알고리즘이 시작되어 끝날 때까지의 실제 시간을 계산하는 것이 아니라
    이 알고리즘이 처리되는데에 필요한 시간복잡도를 계산합니다.

    대표적인 Big-O 의 예로는 총 5가지가 있습니다.
        - 1. O(1) : Constant Time
        - 2. O(n) : Linear Time
        - 3. O(n^2) : Quadratic Time
        - 4. O(2^n)
        - 5. O(log n)

*/